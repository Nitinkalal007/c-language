#include <stdio.h>

int main (){
    /////integer
    int value1=65.77;
    int value2=5;
    printf("%d",value1);
    printf("\n%d",value2);


    ////float
    float f1=45.32;
    printf("\n%f",f1);


    ////character
    char Char='A';
    char smallChar='a';
    printf("\n%c",Char);
    printf("\n%d",Char);
    printf("\n%d",smallChar);


    //////string
    char name[]="web";
    printf("\n%s",name);




    return 0;
}